#include <stdio.h>

int main() {
    // Definição de variáveis
    int numero = 42;          // Inteiro
    float decimal = 3.1415;   // Float
    char caractere = 'A';     // Caractere

    // Impressão usando um único printf
    printf("Número inteiro: %d | Número decimal: %.2f | Caractere: %c\n", numero, decimal, caractere);

    return 0;
}